function tong(n)
        {
            let tam = 0;
            for(i = 0; i <= n; i++)
            {
                tam += i;
            }
            return tam
        }
function tich(n)
        {
            let tam = 0;
            for(i = 0; i <= n; i++)
            {
                tam *= i;
            }
            return tam
        }